check_prime() {
    num=$1
    flag=0

    if [ $num -le 1 ]; then
        echo "$num is not a Prime number"
        return
    fi

    for (( i=2; i<=num/2; i++ ))
    do
        if [ $((num % i)) -eq 0 ]; then
            flag=1
            break
        fi
    done

    if [ $flag -eq 0 ]; then
        echo "$num is a Prime number"
    else
        echo "$num is not a Prime number"
    fi
}

echo "Enter a number:"
read n

check_prime $n
